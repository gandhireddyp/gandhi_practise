	public static final int MAX_20P_COINS = SUM / 20;
	public static final int MAX_50P_COINS = SUM / 50;
	public static final int MAX_100P_COINS = SUM / 100;
	public static final int MAX_200P_COINS = SUM / 200;

	
	
	
